package variable;

public class task3 {

	public static void main(String[] args) {
		int a = 219;
		long d = 219L;
		
		double b = 364.2;
		float e = 364.2F;
		
		long c = 123456;
		double f = 123456.0;
		
		System.out.println("変換前");
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		
	
		System.out.println("変換後");
		
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		
		
	}

}
